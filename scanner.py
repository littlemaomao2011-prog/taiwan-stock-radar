# 1. 取得台股上市櫃股票清單（含名稱對應）
# ==========================================
def get_taiwan_stock_list():
    """使用證交所與櫃買中心官方 CSV 開放資料，精準抓取全台股代碼與名稱"""
    stock_list = []
    headers = {
        "User-Agent": "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36"
    }

    # 1. 上市股票清單
    try:
        twse_url = "https://www.twse.com.tw/exchangeReport/STOCK_DAY_ALL?response=open_data"
        res = requests.get(twse_url, headers=headers, timeout=15)
        if res.status_code == 200:
            lines = res.text.split("\n")
            for line in lines:
                parts = line.split('","')
                if len(parts) > 1:
                    stock_id = parts[0].replace('"', "").strip()
                    stock_name = parts[1].replace('"', "").strip()
                    # 嚴格過濾：只留 4 位數純股票
                    if len(stock_id) == 4 and stock_id.isdigit():
                        stock_list.append(
                            {
                                "stock_id": stock_id,
                                "stock_name": stock_name,
                                "market": "tse",
                            }
                        )
    except Exception as e:
        print(f"⚠️ 上市清單下載失敗: {e}")

    # 2. 上櫃股票清單
    try:
        tpex_url = "https://www.tpex.org.tw/openapi/v1/tpex_mainboard_daily_close_quotes"
        res = requests.get(tpex_url, headers=headers, timeout=15)
        if res.status_code == 200:
            data = res.json()
            for item in data:
                stock_id = item.get("SecuritiesCompanyCode", "").strip()
                stock_name = item.get("CompanyName", "").strip()
                if len(stock_id) == 4 and stock_id.isdigit():
                    stock_list.append(
                        {
                            "stock_id": stock_id,
                            "stock_name": stock_name,
                            "market": "otc",
                        }
                    )
    except Exception as e:
        print(f"⚠️ 上櫃清單下載失敗: {e}")

    # 備用防呆機制
    if not stock_list:
        return [
            {"stock_id": "2330", "stock_name": "台積電", "market": "tse"},
            {"stock_id": "2317", "stock_name": "鴻海", "market": "tse"},
            {"stock_id": "2454", "stock_name": "聯發科", "market": "tse"},
            {"stock_id": "8069", "stock_name": "元太", "market": "otc"},
        ]

    # 移除重複項
    seen = set()
    unique_list = []
    for d in stock_list:
        if d["stock_id"] not in seen:
            seen.add(d["stock_id"])
            unique_list.append(d)

    return unique_list


