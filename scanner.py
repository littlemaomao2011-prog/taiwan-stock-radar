# 2. 60分鐘線核心運算與篩選
# ==========================================
def scan_60m_stock(stock_item):
    stock_id = stock_item["stock_id"]
    stock_name = stock_item["stock_name"]
    market = stock_item["market"]

    yf_code = f"{stock_id}.TWO" if market == "otc" else f"{stock_id}.TW"
    df = yf.download(tickers=yf_code, period="100d", interval="60m", progress=False)

    if df.empty or len(df) < 80:
        return None

    if isinstance(df.columns, pd.MultiIndex):
        df.columns = [col[0].lower() for col in df.columns]
    else:
        df.columns = [col.lower() for col in df.columns]

    for col in ["close", "high", "low", "volume"]:
        df[col] = df[col].astype(float)

    # ==========================================
    # 防控盤機制：計算最近 5 個交易日的日均成交量
    # ==========================================
    recent_5_days_volume = df["volume"].iloc[-25:].sum()
    avg_daily_volume = recent_5_days_volume / 5 / 1000

    MIN_DAILY_VOLUME = 1000  # 🎯 門檻：5日平均量必須大於 1,000 張
    if avg_daily_volume < MIN_DAILY_VOLUME:
        return None

    # ==========================================
    # 🟢 技術指標計算 (VR, 60MA, KD, MACD)
    # ==========================================
    # 1. VR (26)
    df["change"] = df["close"].diff()
    df["vol_up"] = df["volume"].where(df["change"] > 0, 0)
    df["vol_down"] = df["volume"].where(df["change"] < 0, 0)
    df["vol_flat"] = df["volume"].where(df["change"] == 0, 0)

    sum_up = df["vol_up"].rolling(window=26).sum()
    sum_down = df["vol_down"].rolling(window=26).sum()
    sum_flat = df["vol_flat"].rolling(window=26).sum()
    df["VR_26"] = ((sum_up + 0.5 * sum_flat) / (sum_down + 0.5 * sum_flat)) * 100

    # 2. 60MA
    df["ma_60"] = df["close"].rolling(window=60).mean()

    # 3. KD (60, 3, 3)
    kd_df = ta.stoch(
        high=df["high"], low=df["low"], close=df["close"], k=60, d=3, smooth_k=3
    )
    if kd_df is None or kd_df.empty:
        return None
    df["K_60"] = kd_df["STOCHk_60_3_3"]
    df["D_60"] = kd_df["STOCHd_60_3_3"]

    # 4. MACD (12, 26, 9)
    macd_df = ta.macd(close=df["close"], fast=12, slow=26, signal=9)
    if macd_df is None or macd_df.empty:
        return None
    df["macd_dif"] = macd_df["MACD_12_26_9"]
    df["macd_dea"] = macd_df["MACDs_12_26_9"]
    df["macd_hist"] = macd_df["MACDh_12_26_9"]

    today = df.iloc[-1]
    yesterday = df.iloc[-2]

    # ==========================================
    # 🟢 五合一核心策略嚴格過濾
    # ==========================================
    above_ma60 = today["close"] > today["ma_60"]
    kd_golden_cross = (today["K_60"] > today["D_60"]) and (
        yesterday["K_60"] <= yesterday["D_60"]
    )
    kd_strong_zone = (today["K_60"] > 60) and (today["D_60"] > 60)
    vr_healthy = 140 <= today["VR_26"] <= 300
    macd_bullish = today["macd_dif"] > today["macd_dea"]

    # 所有條件同時交集攔截
    if (
        above_ma60
        and (kd_golden_cross or kd_strong_zone)
        and vr_healthy
        and macd_bullish
    ):
        # 🌟 【時區修正】：自動將 Yahoo 的 UTC 時間轉換為台北時間 (+8 小時)
        if df.index[-1].tzinfo is not None:
            tw_time = df.index[-1].tz_convert("Asia/Taipei")
        else:
            tw_time = df.index[-1] + datetime.timedelta(hours=8)

        time_str = tw_time.strftime("%m/%d %H:%M")

        # 🌟 【輸出格式增強】：加入公司名稱與目前最新收盤價
        return (
            f"🔹 *{stock_id} {stock_name}* | 現價: {round(today['close'],2)} | "
            f"60MA: {round(today['ma_60'],2)} | "
            f"60分K值: {round(today['K_60'],1)} | VR: {round(today['VR_26'],1)}% | "
            f"MACD柱: {round(today['macd_hist'],2)} | "
            f"5日日均量: {int(avg_daily_volume)}張 ({time_str})"
        )
    return None


